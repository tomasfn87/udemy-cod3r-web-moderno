// Adição
{
    const a = 1
    let b = 9
    b += a
    console.log(b)
}

// Subtração
{
    const a = 1
    let b = 10
    b -= a
    console.log(b)
}

// Multiplicação
{
    const a = 2
    let b = 4
    b *= a
    console.log(b)
}

// Divisão
{
    const a = 2
    let b = 14
    b /= a
    console.log(b)
}

// Módulo (resto da divisão)
{
    const a = 7
    let b = 69
    b %= a
    console.log(b)
}