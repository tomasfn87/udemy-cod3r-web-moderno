let contador = 1 // declaration
while (contador <= 10) { // condition 
    console.log(`contador = ${contador}`)
    contador++ // increment
}

for (let i = 1;   i <= 10;      i++      ) {   
//   declaration  condition  increment
    console.log(`i = ${i}`)
}

const notas = [6.7, 7.4, 9.8, 8.1, 7.7]
for (i = 0; i < notas.length; i++) {
    console.log(`nota = ${notas[i]}`)
}